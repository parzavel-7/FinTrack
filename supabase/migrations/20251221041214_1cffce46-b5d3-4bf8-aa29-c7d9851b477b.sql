-- Create function to handle new user signup
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER SET search_path = public
AS $$
BEGIN
  INSERT INTO public.profiles (user_id, full_name, email)
  VALUES (NEW.id, NEW.raw_user_meta_data ->> 'full_name', NEW.email);
  
  -- Create default categories for new user
  INSERT INTO public.categories (user_id, name, icon, color, type) VALUES
    (NEW.id, 'Salary', 'Briefcase', '#10b981', 'income'),
    (NEW.id, 'Freelance', 'Laptop', '#3b82f6', 'income'),
    (NEW.id, 'Investments', 'TrendingUp', '#8b5cf6', 'income'),
    (NEW.id, 'Other Income', 'Plus', '#6b7280', 'income'),
    (NEW.id, 'Food & Dining', 'Utensils', '#f59e0b', 'expense'),
    (NEW.id, 'Transportation', 'Car', '#ef4444', 'expense'),
    (NEW.id, 'Shopping', 'ShoppingBag', '#ec4899', 'expense'),
    (NEW.id, 'Entertainment', 'Film', '#8b5cf6', 'expense'),
    (NEW.id, 'Bills & Utilities', 'Receipt', '#6366f1', 'expense'),
    (NEW.id, 'Healthcare', 'Heart', '#10b981', 'expense'),
    (NEW.id, 'Education', 'GraduationCap', '#3b82f6', 'expense'),
    (NEW.id, 'Other Expense', 'MoreHorizontal', '#6b7280', 'expense');
  
  RETURN NEW;
END;
$$;

-- Trigger for new user signup
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

-- Function to update updated_at timestamp
CREATE OR REPLACE FUNCTION public.update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Add triggers for updated_at
CREATE TRIGGER update_profiles_updated_at BEFORE UPDATE ON public.profiles FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();
CREATE TRIGGER update_transactions_updated_at BEFORE UPDATE ON public.transactions FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();
CREATE TRIGGER update_goals_updated_at BEFORE UPDATE ON public.goals FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();